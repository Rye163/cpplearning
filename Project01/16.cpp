#include<iostream>
using namespace std;
 int main (){
	 
	 int a = 10;
	 int b = 20;
	 
	 //比较运算符
	 // ==
	 cout << (a == b) << endl;
	 
	 // !=
	 cout << (a != b) << endl;
	 
	 // <
	 cout << (a < b) << endl;
	 
	 // >
	 cout << (a > b) << endl;
	 
	 // <=
	 cout << (a <= b) << endl;
	 
	 // >=
	 cout << (a >= b) << endl;
	 
	 system("pause");
	 return 0;
 }
